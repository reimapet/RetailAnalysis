package com.affecto.retail;

public class PeopleCountData {
	private long timestamp;
	private long observedInterval;
	private long observedPeopleTotal;
	
	
	public long getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(long timestamp) {
		this.timestamp = timestamp;
	}
	public long getObservedInterval() {
		return observedInterval;
	}
	public void setObservedInterval(long observedInterval) {
		this.observedInterval = observedInterval;
	}
	public long getObservedPeopleTotal() {
		return observedPeopleTotal;
	}
	public void setObservedPeopleTotal(long observedPeopleTotal) {
		this.observedPeopleTotal = observedPeopleTotal;
	}
	
	
	
}
